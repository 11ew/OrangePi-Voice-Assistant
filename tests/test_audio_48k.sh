#!/bin/bash
# -*- coding: utf-8 -*-
#
# 音频录音和播放完整测试脚本（48000 Hz）
# 作者：哈雷酱（傲娇大小姐工程师）
# 用途：测试 Orange Pi 的录音和播放功能
#

set -e  # 遇到错误立即退出

echo "============================================================"
echo "🎙️  音频录音和播放测试（48000 Hz）"
echo "   作者：哈雷酱（傲娇大小姐工程师）"
echo "============================================================"
echo ""

# 测试文件路径
PCM_FILE="test_48k.pcm"
WAV_FILE="test_48k.wav"

# ============================================================
# 步骤 1：设置音频参数
# ============================================================
echo "📋 步骤 1/5: 设置音频参数..."
echo ""

echo "  设置录音音量 (Capture = 10)..."
amixer set Capture 10 > /dev/null

echo "  设置设备 ID (Deviceid = 2)..."
amixer set Deviceid 2 > /dev/null

echo "  设置播放音量 (Playback = 10)..."
amixer set Playback 10 > /dev/null

echo "✅ 音频参数设置完成！"
echo ""

# ============================================================
# 步骤 2：录音测试
# ============================================================
echo "============================================================"
echo "🎤 步骤 2/5: 录音测试（5 秒）"
echo "============================================================"
echo ""
echo "⚠️  请对着麦克风清晰地说话..."
echo "   （录音将在 3 秒后开始）"
echo ""

sleep 1
echo "3..."
sleep 1
echo "2..."
sleep 1
echo "1..."
sleep 1
echo ""
echo "🔴 开始录音！（5 秒）"
echo ""

# 使用 RAW 格式录音（避免 Orange Pi 的 WAV bug）
arecord -D plughw:0,1 -f S16_LE -r 48000 -c 1 -t raw -d 5 "$PCM_FILE"

echo ""
echo "✅ 录音完成！"
echo ""

# ============================================================
# 步骤 3：转换为 WAV 格式
# ============================================================
echo "============================================================"
echo "🔄 步骤 3/5: 转换为 WAV 格式"
echo "============================================================"
echo ""

ffmpeg -f s16le -ar 48000 -ac 1 -i "$PCM_FILE" -y "$WAV_FILE" 2>&1 | grep -E "(Input|Output|Duration|size|time)" || true

echo ""
echo "✅ 转换完成！"
echo ""

# ============================================================
# 步骤 4：验证音频质量
# ============================================================
echo "============================================================"
echo "📊 步骤 4/5: 验证音频质量"
echo "============================================================"
echo ""

python3 << 'EOF'
import wave
import numpy as np

with wave.open('test_48k.wav', 'rb') as wf:
    audio_data = wf.readframes(wf.getnframes())
    samples = np.frombuffer(audio_data, dtype=np.int16)
    max_val = np.max(np.abs(samples))
    mean_val = np.mean(np.abs(samples))

    print(f"  音频最大值: {max_val}")
    print(f"  音频平均值: {mean_val:.2f}")
    print(f"  样本数量: {len(samples)}")
    print(f"  时长: {len(samples) / 48000:.2f} 秒")
    print("")

    if max_val < 100:
        print("❌ 音频音量异常低！可能录音失败！")
    elif max_val < 1000:
        print("⚠️  音频音量较低，建议降低 Capture 值（如改为 5）")
    else:
        print("✅ 音频音量正常！")
EOF

echo ""

# ============================================================
# 步骤 5：播放录音
# ============================================================
echo "============================================================"
echo "🔊 步骤 5/5: 播放录音"
echo "============================================================"
echo ""
echo "⚠️  请戴上耳机，准备听录音..."
echo "   （播放将在 3 秒后开始）"
echo ""

sleep 1
echo "3..."
sleep 1
echo "2..."
sleep 1
echo "1..."
sleep 1
echo ""
echo "🔊 开始播放！"
echo ""

# 播放录音
aplay -Dhw:ascend310b "$WAV_FILE"

echo ""
echo "✅ 播放完成！"
echo ""

# ============================================================
# 测试完成
# ============================================================
echo "============================================================"
echo "🎉 测试完成！"
echo "============================================================"
echo ""
echo "📝 测试文件："
echo "   - PCM 文件: $PCM_FILE"
echo "   - WAV 文件: $WAV_FILE"
echo ""
echo "🔍 请确认以下问题："
echo "   1. 录音过程是否正常？（无错误提示）"
echo "   2. 能否听到自己刚才说的话？"
echo "   3. 录音是否清晰？（无失真、无"魔音"效果）"
echo "   4. 音量是否合适？（不过大或过小）"
echo ""
echo "如果一切正常，请告诉本小姐，我就可以修改项目代码了！(￣▽￣)ノ"
echo ""
